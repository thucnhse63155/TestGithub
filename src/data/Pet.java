package data;

import java.util.Scanner;

/**
 *
 * @author MY_PC
 */
public abstract class Pet {
    protected String name;
    protected String id;
    protected int yob;
    protected double weight;

    public Pet(String name, int yob, double weight,String id) {
        this.name = name;
        this.yob = yob;
        this.weight = weight;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYob() {
        return yob;
    }

    public void setYob(int yob) {
        this.yob = yob;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
    public abstract void showDetailed();
}
